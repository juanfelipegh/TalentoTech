
// 1. Suma de tres números
let num1 = 10;
let num2 = 20;
let num3 = 30;
let suma = num1 + num2 + num3;
console.log("Suma de tres números:", suma);  // Resultado: 60

// 2. Multiplicación de dos decimales
let decimal1 = 5.5;
let decimal2 = 2.3;
let multiplicacion = decimal1 * decimal2;
console.log("Multiplicación de dos decimales:", multiplicacion);  // Resultado: 12.65

// 3. Promedio de cuatro notas
let nota1 = 85;
let nota2 = 90;
let nota3 = 78;
let nota4 = 88;
let promedio = (nota1 + nota2 + nota3 + nota4) / 4;
console.log("Promedio de cuatro notas:", promedio);  // Resultado: 85.25

// 4. Área de un triángulo
let base = 10;
let altura = 5;
let areaTriangulo = (base * altura) / 2;
console.log("Área de un triángulo:", areaTriangulo);  // Resultado: 25

// 5. Raíz cuadrada de un número
let numero = 16;
let raizCuadrada = Math.sqrt(numero);
console.log("Raíz cuadrada de un número:", raizCuadrada);  // Resultado: 4

// 6. Conversión de grados Celsius a Fahrenheit
let celsius = 25;
let fahrenheit = (celsius * (9/5)) + 32;
console.log("Conversión de Celsius a Fahrenheit:", fahrenheit);  // Resultado: 77

// 7. Potencia de un número
let baseNum = 3;
let exponente = 4;
let potencia = baseNum ** exponente;
console.log("Potencia de un número:", potencia);  // Resultado: 81

// 8. Concatenación de nombres
let nombre = "Juan";
let apellido = "Pérez";
let nombreCompleto = nombre + " " + apellido;
console.log("Concatenación de nombres:", nombreCompleto);  // Resultado: Juan Pérez

// 9. Calcular el perímetro de un círculo
let radio = 7;
let perimetro = 2 * Math.PI * radio;
console.log("Perímetro de un círculo:", perimetro);  // Resultado: 43.982297150257104

// 10. Restar dos números grandes
let numeroGrande1 = 50000;
let numeroGrande2 = 12000;
let resta = numeroGrande1 - numeroGrande2;
console.log("Resta de dos números grandes:", resta);  // Resultado: 38000
